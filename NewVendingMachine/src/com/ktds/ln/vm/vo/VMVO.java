package com.ktds.ln.vm.vo;

public class VMVO {

	private String drinkName;
	private int numOfDrink;
	private int costPerOneDrink;
	
	public String getDrinkName() {
		return drinkName;
	}
	public void setDrinkName(String drinkName) {
		this.drinkName = drinkName;
	}
	public int getNumOfDrink() {
		return numOfDrink;
	}
	public void setNumOfDrink(int numOfDrink) {
		this.numOfDrink = numOfDrink;
	}
	public int getCostPerOneDrink() {
		return costPerOneDrink;
	}
	public void setCostPerOneDrink(int costPerOneDrink) {
		this.costPerOneDrink = costPerOneDrink;
	}
	
	
	
}
